    public static List<NumberDTO> buildAllNumberLoDe() {
        List<NumberDTO> listData = new ArrayList<>();
        for (int i = 0; i < 100; i++) {
            String value;
            if (i < 10) {
                value = "0" + i;
            } else {
                value = i + "";
            }
            NumberDTO numberDTO = new NumberDTO();
            numberDTO.setValue(value);
            numberDTO.setSelected(false);

            listData.add(numberDTO);
        }
        return listData;
    }

